#Operações Matemáticas 

"""
1.Soma = +
2.Subtração = -
3.Multiplicação = *
4.Divisão = /
5.Divisão inteira = //
6.Resto de divisão = %
7.Potência = **

"""
numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1*numero2)
print(numero1/numero2)
print(numero1//numero2) #exclui a parte decimal
print(numero1%numero2)
print(numero1**numero2)

print(numero1<numero2)
print(numero1>numero2)
print(numero1==numero2)
print(numero1!=numero2)
